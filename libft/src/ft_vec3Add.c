#include "libft.h"

t_vec				ft_vec3Add(t_vec v1, t_vec v2)
{
	t_vec			add;

	add.x = v1.x + v2.x;
	add.y = v1.y + v2.y;
	add.z = v1.z + v2.z;
	return (add);
}
